function applyPatch(locale, data) {
  const bf = BOOKING_FLOW[locale] ?? BOOKING_FLOW[FALLBACK_LOCALE[locale] ?? "fr"];
  const cm = COMMON_MAP[locale] ?? COMMON_MAP[FALLBACK_LOCALE[locale] ?? "fr"];
  if (!data.BookingFlow) data.BookingFlow = {};
  if (!data.Common) data.Common = {};
  Object.assign(data.BookingFlow, bf);
  Object.assign(data.Common, cm);
}

for (const file of fs.readdirSync(MESSAGES)) {
  if (!file.endsWith(".json") || file === "en.json") continue;
  const locale = file.replace(".json", "");
  const filePath = path.join(MESSAGES, file);
  const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
  applyPatch(locale, data);
  fs.writeFileSync(filePath, `${JSON.stringify(data, null, 2)}\n`, "utf8");
}

